'use client'
// src/app/exam/[versionId]/components/ExamHeader.tsx
import { Layout, Button, Typography, Space, Row, Col } from 'antd';
import { 
  ClockCircleOutlined, 
  SaveOutlined, 
  CheckCircleOutlined,
  QuestionCircleOutlined,
  TrophyOutlined 
} from '@ant-design/icons';
import React from 'react';

const { Header } = Layout;
const { Title, Text } = Typography;

// ƒê·ªãnh nghƒ©a c√°c ki·ªÉu d·ªØ li·ªáu
type ExamStatus = 'not-started' | 'in-progress' | 'submitted' | 'time-up';

interface ExamHeaderProps {
  versionId: string;
  timeLeft: number;
  onSubmit: () => void;
  examStatus: ExamStatus;
  totalQuestions?: number;
  answeredCount?: number;
}

const formatTime = (seconds: number): string => {
  const minutes = Math.floor(seconds / 60);
  const remainingSeconds = seconds % 60;
  return `${String(minutes).padStart(2, '0')}:${String(remainingSeconds).padStart(2, '0')}`;
};

const ExamHeader: React.FC<ExamHeaderProps> = ({ 
  versionId, 
  timeLeft, 
  onSubmit, 
  examStatus,
  totalQuestions = 0,
  answeredCount = 0 
}) => {
  const isFinished = examStatus === 'submitted' || examStatus === 'time-up';
  const isTimeWarning = timeLeft <= 300 && !isFinished; // 5 minutes warning
  const isTimeCritical = timeLeft <= 60 && !isFinished; // 1 minute critical

  const getTimeColor = () => {
    if (isFinished) return '#52c41a';
    if (isTimeCritical) return '#ff4d4f';
    if (isTimeWarning) return '#fa8c16';
    return '#1890ff';
  };

  return (
    <Header
      style={{
        background: '#fff',
        padding: '0 24px',
        borderBottom: '2px solid #f0f0f0',
        boxShadow: '0 2px 8px rgba(0,0,0,0.06)',
        position: 'fixed', // S·ª≠ d·ª•ng fixed ƒë·ªÉ header lu√¥n c·ªë ƒë·ªãnh
        top: 0,
        left: 0,
        right: 0,
        zIndex: 1000,
        height: '80px',
        lineHeight: 'normal',
        display: 'flex',
        alignItems: 'center'
      }}
    >
      <Row style={{ width: '100%' }} justify="space-between" align="middle">
        {/* Left: Title and Progress */}
        <Col flex="auto">
          <div>
            <Title level={4} style={{ margin: 0, color: '#1890ff' }}>
              üéØ ƒê·ªÅ thi m√£ s·ªë {versionId}
            </Title>
            <div style={{ display: 'flex', alignItems: 'center', gap: '16px', marginTop: '8px' }}>
              <Space size="middle">
                <div style={{ display: 'flex', alignItems: 'center', gap: '4px' }}>
                  <CheckCircleOutlined style={{ color: '#52c41a', fontSize: '14px' }} />
                  <Text style={{ fontSize: '14px', color: '#52c41a', fontWeight: 'bold' }}>
                    {answeredCount}
                  </Text>
                </div>
                <div style={{ display: 'flex', alignItems: 'center', gap: '4px' }}>
                  <QuestionCircleOutlined style={{ color: '#d9d9d9', fontSize: '14px' }} />
                  <Text style={{ fontSize: '14px', color: '#8c8c8c', fontWeight: 'bold' }}>
                    {totalQuestions - answeredCount}
                  </Text>
                </div>
              </Space>
            </div>
          </div>
        </Col>

        {/* Right: Timer and Submit */}
        <Col>
          <div style={{ display: 'flex', alignItems: 'center', gap: '24px' }}>
            {/* Timer */}
            <div style={{ 
              display: 'flex', 
              flexDirection: 'column', 
              alignItems: 'center',
              padding: '8px 16px',
              // Kh√¥ng c·∫ßn position: sticky ·ªü ƒë√¢y n·ªØa
              background: isFinished ? '#f6ffed' : 
                         isTimeCritical ? '#fff2f0' : 
                         isTimeWarning ? '#fff7e6' : '#e6f7ff',
              borderRadius: '8px',
              border: `2px solid ${getTimeColor()}`,
              minWidth: '120px'
            }}>
              <div style={{ 
                fontSize: '20px', 
                fontWeight: 'bold', 
                color: getTimeColor(),
                display: 'flex',
                alignItems: 'center',
                gap: '6px'
              }}>
                {isFinished ? <TrophyOutlined /> : <ClockCircleOutlined />}
                {isFinished ? 'Ho√†n th√†nh' : formatTime(timeLeft)}
              </div>
              <Text 
                style={{ 
                  fontSize: '11px', 
                  color: getTimeColor(),
                  fontWeight: 'bold',
                  textTransform: 'uppercase'
                }}
              >
                {isFinished ? 'ƒê√£ n·ªôp b√†i' : 
                 isTimeCritical ? 'Nguy hi·ªÉm!' : 
                 isTimeWarning ? 'C·∫£nh b√°o!' : 'Th·ªùi gian'}
              </Text>
            </div>

            {/* Submit Button */}
            <Button
              type="primary"
              size="large"
              icon={<SaveOutlined />}
              onClick={onSubmit}
              disabled={isFinished}
              style={{
                height: '50px',
                fontSize: '16px',
                fontWeight: 'bold',
                minWidth: '120px',
                background: isFinished ? '#52c41a' : undefined,
                borderColor: isFinished ? '#52c41a' : undefined
              }}
            >
              {isFinished ? '‚úÖ ƒê√£ n·ªôp' : 'üì§ N·ªôp b√†i'}
            </Button>
          </div>
        </Col>
      </Row>
    </Header>
  );
};

export default ExamHeader;